public class Operator{
public static void main(String[] args){
int number= 600;
//== it checks the values
System.out.println(number==600);

//!= not equal to
System.out.println(number!=600);

//>= greater than or equal to//
System.out.println(number>=600);

//<= less than or equal to
System.out.println(number<=600);

//< greater than
System.out.println(number>600);

//> less than
System.out.println(number<800);
}}